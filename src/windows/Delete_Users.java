  private void btn_SearchIDActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_Eliminar1ActionPerformed
    ID = Integer.parseInt(JOptionPane.showInputDialog("Type id from user: "));
    try {
      Connection cn = Connection_DB.connect();
      PreparedStatement pst = cn.prepareStatement(
          "select * from Usuarios where Id_Usuario = " + ID + "");
      ResultSet rs = pst.executeQuery();

      if (rs.next()) {
        flag = true;
        ID = rs.getInt("Id_Usuario");

        txt_Name.setText(rs.getString("Nombre"));
        txt_Name.setEnabled(false);
        txt_mail.setText(rs.getString("email"));
        txt_mail.setEnabled(false);
        txt_Phone.setText(rs.getString("telefono"));
        txt_Phone.setEnabled(false);
        txt_Username.setText(rs.getString("username"));
        txt_Username.setEnabled(false);
        txt_Registered.setText(rs.getString("registrado_por"));
        txt_Registered.setEnabled(false);
        txt_Status.setText(rs.getString("estatus"));
        txt_Status.setEnabled(false);
      } else {
        JOptionPane.showMessageDialog(null, "There isn't user with this ID!");
      }
      cn.close();
    } catch (SQLException e) {
      System.err.println("Failed to load user! " + e);
      JOptionPane.showMessageDialog(null, "Failed to load user!");
    }

  }// GEN-LAST:event_btn_Eliminar1ActionPerformed

  private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_EliminarActionPerformed
    if (flag) {
      try {

        Connection cn = Connection_DB.connect();
        PreparedStatement pst = cn.prepareStatement(
            "delete from Usuarios where Id_Usuario = " + ID + "");
        pst.executeUpdate();

        txt_mail.setBackground(Color.GREEN);
        txt_Username.setBackground(Color.GREEN);
        txt_Name.setBackground(Color.GREEN);
        txt_Phone.setBackground(Color.GREEN);
        JOptionPane.showMessageDialog(null, "Deleted user");
        this.dispose();

      } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Failed to delete user");
        System.err.println("Failed to delete user " + e);
      }
    } else {
      JOptionPane.showMessageDialog(null, "You have to search for a user by id to delete it");
    }

  }// GEN-LAST:event_btn_EliminarActionPerformed

