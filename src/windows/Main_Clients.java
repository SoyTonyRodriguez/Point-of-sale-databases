    public Main_Clients() {
        
        
        initComponents();
        user = Login.user;
        sesion_usuario = 1;

        setSize(435, 262);
        setResizable(false);
        setTitle("Client - " + user + " session");
        setLocationRelativeTo(null);

        setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);

        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(lbl_wallpaper.getWidth(),
                lbl_wallpaper.getHeight(), Image.SCALE_DEFAULT));

        lbl_wallpaper.setIcon(icono);
        this.repaint();

        try {
            Connection cn = Connection_DB.connect();
            PreparedStatement pst = cn.prepareStatement(
                    "select Nombre from Usuarios where username = '" + user + "'");

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                nombre_usuario = rs.getString("Nombre");
                lbl_NombreUsuario.setText("Welcome@ "+nombre_usuario);
            }
        } catch (Exception e) {
            System.err.println("Failed connection from client interface" +e);
        }
        

    }
    
