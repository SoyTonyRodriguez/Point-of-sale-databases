  public Orders() {
    initComponents();

    id = ID.Id_Cliente;
    user = Login.user;

    setSize(630, 330);
    setTitle("Orders - " + user + " session");
    setResizable(false);
    setLocationRelativeTo(null);

    setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

    ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
    Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(lbl_wallpaper.getWidth(),
        lbl_wallpaper.getHeight(), Image.SCALE_DEFAULT));

    lbl_wallpaper.setIcon(icono);
    this.repaint();

    try {
      Connection cn = Connection_DB.connect();
      //PreparedStatement pst = cn.prepareStatement(
      //    "select id_pedido, Producto, total from pedidos where Id_Usuario = " + id + ";");
      
      PreparedStatement pst = cn.prepareStatement(
              "select id_pedido, Producto, total from pedidos where id_Usuario=(select "
                      + "Id_Usuario from Usuarios where username = '"+ Login.user + "')");
      ResultSet rs = pst.executeQuery();

      jTable_Orders = new JTable(model);
      jScrollPane1.setViewportView(jTable_Orders);

      model.addColumn("ID");
      model.addColumn("Producto");
      model.addColumn("total");

      while (rs.next()) {

        Object[] fila = new Object[3];
        for (int i = 0; i < 3; i++) {
          fila[i] = rs.getObject(i + 1);
        }
        model.addRow(fila);

      }
      cn.close();

    } catch (SQLException e) {
      System.err.println("Error filling the table" + e);
      JOptionPane.showMessageDialog(null, "Error filling the table");
    }

  }

