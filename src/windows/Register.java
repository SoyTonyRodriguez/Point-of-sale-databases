  private void btn_RegisterActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_RegisterActionPerformed
    int flag = 0;
    String name, mail, phone, username, pass, status = "";

    name = txt_name.getText().trim();// .trim elimina espacios al inicio y al final
    mail = txt_mail.getText().trim();
    phone = txt_Phone.getText().trim();
    username = txt_username.getText().trim();
    pass = txt_password.getText().trim();
    if (rdb_Cliente.isSelected()) {
      status = "Cliente";
    } else if (rdb_Administrador.isSelected()) {
      status = "Administrador";
    }

    if (mail.equals("")) {
      txt_mail.setBackground(Color.red);
      flag++;
    }
    if (username.equals("")) {
      txt_username.setBackground(Color.red);
      flag++;
    }
    if (pass.equals("")) {
      txt_password.setBackground(Color.red);
      flag++;
    }
    if (name.equals("")) {
      txt_name.setBackground(Color.red);
      flag++;
    }
    if (phone.equals("")) {
      txt_Phone.setBackground(Color.red);
      flag++;
    }
    if (status.equals("")) {
      rdb_Cliente.setBackground(Color.red);
      rdb_Administrador.setBackground(Color.red);
    }

    try {
      Connection cn = Connection_DB.connect();
      PreparedStatement pst = cn.prepareStatement(
          "select username from Usuarios where username = '" + username + "'");
      ResultSet rs = pst.executeQuery();

      if (rs.next()) {
        txt_username.setBackground(Color.red);
        JOptionPane.showMessageDialog(null, "Username not available");
        cn.close();
      } else {

        cn.close();

        if (flag == 0) {
          try {

            Connection cn2 = Connection_DB.connect();
            PreparedStatement pst2 = cn2.prepareStatement(
                "insert into Usuarios values (?,?,?,?,?,?,?,?)");

            // Add values of the database in insert query
            pst2.setInt(1, 0);
            pst2.setString(2, name);
            pst2.setString(3, mail);
            pst2.setString(4, phone);
            pst2.setString(5, username);
            pst2.setString(6, pass);
            pst2.setString(7, status);
            pst2.setString(8, user);

            // Execute the query 
            pst2.executeUpdate();
            cn2.close();

            clear();

            txt_mail.setBackground(Color.GREEN);
            txt_username.setBackground(Color.GREEN);
            txt_password.setBackground(Color.GREEN);
            txt_name.setBackground(Color.GREEN);
            txt_Phone.setBackground(Color.GREEN);
            rdb_Cliente.setBackground(Color.GREEN);
            rdb_Administrador.setBackground(Color.GREEN);

            JOptionPane.showMessageDialog(null, "Successful register");
            this.dispose();

          } catch (SQLException e) {
            System.err.println("Error registering user" + e);
            JOptionPane.showMessageDialog(null, "Error registering user!");

          }
        } else {
          JOptionPane.showMessageDialog(null, "You have to fill all the fields");
          Color color = new Color(93, 93, 212);
          txt_mail.setBackground(color);
          txt_username.setBackground(color);
          txt_password.setBackground(color);
          txt_name.setBackground(color);
          txt_Phone.setBackground(color);
          rdb_Cliente.setBackground(color);
          rdb_Administrador.setBackground(color);
        }
      }
    } catch (SQLException e) {
      System.err.println("Error validating username: " + e);
      JOptionPane.showMessageDialog(null, "Â¡Error validating username!!");
    }

  }// GEN-LAST:event_btn_RegisterActionPerformed

