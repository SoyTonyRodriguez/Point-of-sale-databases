    private void btn_RestaurarPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RestaurarPasswordActionPerformed
        String password, confirm_Password;

        password = pasf_Password.getText().trim();
        confirm_Password = pasf_Confirmation.getText().trim();

        if (!password.equals("") && !confirm_Password.equals("")) {
            if (password.equals(confirm_Password)) {

                try {
                    Connection cn = Connection_DB.connect();
                    PreparedStatement pst= cn.prepareStatement(
                        "update Usuarios set password=? where username = '"+ user_update+"'");

                    pst.setString(1, password);

                    pst.executeUpdate();
                    cn.close();
                    pasf_Password.setBackground(Color.GREEN);
                    pasf_Confirmation.setBackground(Color.GREEN);

                    JOptionPane.showMessageDialog(null, "Successful restore!");
                    this.dispose();

                } catch (SQLException e) {
                    System.out.println("Errror en restaurar password "+ e);
                    System.out.println("Failed password restore!"+ e);
                }

            } else {
                pasf_Confirmation.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "The passwords have to be the same");
            }

        } else {
            pasf_Password.setBackground(Color.red);
            pasf_Confirmation.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "You can't hava an empty password!");
        }

    }//GEN-LAST:event_btn_RestaurarPasswordActionPerformed

