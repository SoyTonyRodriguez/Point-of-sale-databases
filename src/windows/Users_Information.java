  private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_UpdateActionPerformed
    int validation = 0;
    String name, mail, phone, username, status = "";

    mail = txt_mail.getText().trim();
    username = txt_Username.getText().trim();
    name = txt_Name.getText().trim();
    phone = txt_Password.getText().trim();

    if (mail.equals("")) {
      txt_mail.setBackground(Color.red);
      validation++;
    }
    if (username.equals("")) {
      txt_Username.setBackground(Color.red);
      validation++;
    }
    if (name.equals("")) {
      txt_Name.setBackground(Color.red);
      validation++;
    }
    if (phone.equals("")) {
      txt_Password.setBackground(Color.red);
      validation++;
    }

    if (rdb_Client.isSelected()) {
      status = "Cliente";
    } else if (rdb_Administrator.isSelected()) {
      status = "Administrador";
    }

    if (validation == 0 && status != "") {

      try {

        Connection cn = Connection_DB.connect();
        PreparedStatement pst = cn.prepareStatement(
            "select username from Usuarios where username = '" + username + "' and not Id_Usuario = '" + ID + "'");
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {

          txt_Username.setBackground(Color.red);
          JOptionPane.showMessageDialog(null, "Username not available");
          cn.close();

        } else {
          Connection cn2 = Connection_DB.connect();
          PreparedStatement pst2 = cn2.prepareStatement(
              "update Usuarios set Nombre=?, email=?, telefono=?, username=?, estatus=? "
                  + "where Id_Usuario = '" + ID + "'");

          pst2.setString(1, name);
          pst2.setString(2, mail);
          pst2.setString(3, phone);
          pst2.setString(4, username);
          pst2.setString(5, status);

          pst2.executeUpdate();
          cn2.close();

          JOptionPane.showMessageDialog(null, "Succesful update");
          dispose();
        }

      } catch (SQLException e) {
        System.err.println("Failed to update " + e);
      }

    } else {
      JOptionPane.showMessageDialog(null, "You have to fill all the fields");
    }

  }// GEN-LAST:event_btn_UpdateActionPerformed

